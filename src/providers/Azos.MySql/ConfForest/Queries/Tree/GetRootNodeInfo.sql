select
  0x000000000000000000000001 AS GDID,
  TL.GDID AS G_VERSION,
  0x000000000000000000000001 AS G_PARENT,
  TL.VERSION_UTC,
  TL.VERSION_ORIGIN,
  TL.VERSION_ACTOR,
  TL.VERSION_STATE,
  '/' AS PATH_SEGMENT,
  TL.START_UTC,
  TL.PROPERTIES,
  TL.CONFIG
from
  tbl_node TN inner join tbl_nodelog TL on TN.GDID = TL.G_NODE
where
  (TN.GDID = 0x000000000000000000000001) AND
  (TL.G_NODE = 0x000000000000000000000001) AND
  (TL.START_UTC <= @asof)
order by
 TL.START_UTC desc, TL.VERSION_UTC desc
limit 1
